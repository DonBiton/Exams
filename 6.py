a = 13
b = 5

# Сложение
sum_result = a + b

# Вычитание
sub_result = a - b

# Умножение
mul_result = a * b

# Деление
div_result = a / b

# Целочисленное деление
int_div_result = a // b

# Остаток от деления
mod_result = a % b

# Возведение в степень
power_result = a ** b

print(f"Сложение: {sum_result}")
print(f"Вычитание: {sub_result}")
print(f"Умножение: {mul_result}")
print(f"Деление: {div_result}")
print(f"Целочисленное деление: {int_div_result}")
print(f"Остаток от деления: {mod_result}")
print(f"Возведение в степень: {power_result}")
